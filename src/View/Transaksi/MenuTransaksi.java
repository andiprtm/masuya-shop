    private void deleteSelectedTransaksi() {
        int row = tabel_transaksi.getSelectedRow();
        if (row < 0) return;
        String noInv = tabel_transaksi.getValueAt(row, 0).toString();
        int confirm = JOptionPane.showConfirmDialog(this,
            String.format("Hapus transaksi %s?", noInv),
            "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            if (controller.deleteTransaksi(noInv)) {
                JOptionPane.showMessageDialog(this,
                    String.format("Transaksi %s berhasil dihapus.", noInv));
                loadTransaksiToTable();
            } else {
                JOptionPane.showMessageDialog(this,
                    "Gagal menghapus transaksi.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private void editSelectedTransaksi() {
        int row = tabel_transaksi.getSelectedRow();
        if (row < 0) return;
        String noInv = tabel_transaksi.getValueAt(row, 0).toString();
        AddOrEditTransaksi frame = new AddOrEditTransaksi(noInv);
        frame.setVisible(true);
        java.awt.Window w = javax.swing.SwingUtilities.getWindowAncestor(this);
        if (w != null) w.dispose();
    }
    
