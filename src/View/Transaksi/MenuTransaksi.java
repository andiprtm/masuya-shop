    private void loadTransaksiToTable() {
        List<FullTransaksi> list = controller.getAllFullTransaksi();
        String[] columnNames = {"No Invoice", "Kode Cust", "Nama Cust", "Alamat Cust", "Tgl Inv", "Total"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);

        for (FullTransaksi ft : list) {
            Object[] row = {
                ft.getNoInv(),
                ft.getKodeCust(),
                ft.getNamaCust(),
                ft.getAlamatCust(),
                Helper.formatDate(ft.getTglInv()),    // format dd-MM-yyyy
                Helper.formatRupiah(ft.getTotal()) 
            };
            model.addRow(row);
        }

        // Set model dan pengaturan tampilan
        tabel_transaksi.setModel(model);
        tabel_transaksi.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tabel_transaksi.setFillsViewportHeight(true);
        tabel_transaksi.setBackground(Color.WHITE);
        jScrollPane1.getViewport().setBackground(Color.WHITE);

        // Resize kolom otomatis
        resizeColumnWidth(tabel_transaksi);
        btn_delete.setEnabled(false);
        btn_edit.setEnabled(false);
    }

    /**
     * Resize tiap kolom agar muat konten terpanjang.
     */
    private void resizeColumnWidth(JTable table) {
        final TableColumnModel columnModel = table.getColumnModel();
        for (int col = 0; col < table.getColumnCount(); col++) {
            int maxWidth = 50;
            for (int row = 0; row < table.getRowCount(); row++) {
                TableCellRenderer renderer = table.getCellRenderer(row, col);
                Component comp = renderer.getTableCellRendererComponent(
                    table,
                    table.getValueAt(row, col),
                    false, false,
                    row, col
                );
                maxWidth = Math.max(maxWidth, comp.getPreferredSize().width);
            }
            columnModel.getColumn(col).setPreferredWidth(maxWidth + 10);
        }
    }

