    private void loadProdukToTable() {
        List<Produk> list = controller.getAllProduk();
        // Define columns
        String[] columnNames = {"Kode", "Nama", "Harga", "Stok"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);

        // Populate rows
        for (Produk p : list) {
            Object[] rowData = {
                p.getKode(),
                p.getNama(),
                Helper.formatRupiah(p.getHarga()),
                p.getStok()
            };
            model.addRow(rowData);
        }

        // Apply model to the custom table
        table_produk.setModel(model);
        // Reset tombol
        btn_edit.setEnabled(false);
        btn_delete.setEnabled(false);
    }

    /**
     * Hapus produk terpilih.
     */
    private void deleteSelectedProduk() {
        int row = table_produk.getSelectedRow();
        if (row < 0) {
            return;
        }
        String kode = table_produk.getValueAt(row, 0).toString();
        OperationResult res = controller.deleteProduk(kode);
        if (res.isSuccess()) {
            loadProdukToTable();
        } else {
            JOptionPane.showMessageDialog(this, res.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    /**
     * Edit produk terpilih (buka form dengan data terisi).
     */
    private void editSelectedProduk() {
        int row = table_produk.getSelectedRow();
        if (row < 0) {
            return;
        }
        String kode = table_produk.getValueAt(row, 0).toString();
        AddOrEditProduct frame = new AddOrEditProduct(kode);
        frame.setVisible(true);
        // tutup parent window jika perlu
        java.awt.Window win = javax.swing.SwingUtilities.getWindowAncestor(this);
        if (win != null) {
            win.dispose();
        }
    }

    private void resizeColumnWidth(JTable table) {
        final TableColumnModel columnModel = table.getColumnModel();
        for (int col = 0; col < table.getColumnCount(); col++) {
            int maxWidth = 50;
            for (int row = 0; row < table.getRowCount(); row++) {
                TableCellRenderer renderer = table.getCellRenderer(row, col);
                Component comp = renderer.getTableCellRendererComponent(
                        table,
                        table.getValueAt(row, col),
                        false, false, row, col);
                maxWidth = Math.max(maxWidth, comp.getPreferredSize().width);
            }
            columnModel.getColumn(col).setPreferredWidth(maxWidth + 10);
        }
    }

